// soma valores
function sum(a, b) {
    return a + b;
}

// subtrai valores
function sub(a, b) {
    return a - b;
}

// multiplica valores
function times(a, b) {
    return a * b;
}

// divide valores
function div(a, b) {
    return a / b;
}

function div2(a) {
    return a / 2;
}

module.exports = { sum, sub, times, div, div2 };
